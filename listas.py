amigos = ['Bia', 'Lua', 'Maria', 'Julia', 'Lola']

# Imprimindo a lista
print(amigos)

# Imprimindo a partir de um index
print(amigos[2])

# Contando de trás pra frente
print(amigos[-2])

# Pegando mais de um item
print(amigos[0:3])

# A partir de uma posição em diante
print(amigos[2:])

# Modificando itens a partir de uma posição
print(amigos)
amigos[1] = 'Fran'
print(amigos)

