# fazendo quebra de linhas usando \n
print('Maria \nCarrara')

# adicionando aspas simples em um texto
print('Maria Carrara\'')

# imprimindo nome através da variável e concatenando
nome = 'Maria Carrara'
print(nome)

# usando as funções de uma string, neste caso convertendo para letra maiúscula
print(nome.upper())

# usando as funções de uma string para contar quantos caracteres tem na string
print(len(nome))

# filtrando valores dentro da string
print(nome[3])

# filtrando valores a partir de um valor informado e indicando a posição inicial
print(nome.index('rr'))

# conta quantas letras existe na frase
print(nome.count('a'))

# conta quantas letras existe na frase
print(nome.__dict__)
